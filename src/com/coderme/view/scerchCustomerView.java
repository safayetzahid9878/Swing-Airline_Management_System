    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            
           AddCustomer customer = (AddCustomer) commonService.get(Integer.parseInt(txtCustId.getText()));           
            txtFirstname.setText(String.valueOf(customer.getFirst_name()));
           txtLastname.setText(String.valueOf(customer.getLast_name()));
            txtNid.setText(String.valueOf(customer.getNid()));
           txtPassport.setText(String.valueOf(customer.getPass_id()));
           txtAddress.setText(String.valueOf(customer.getAddress()));
           
            txtContact.setText(String.valueOf(customer.getContact()));
             
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "No data has been pond");
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

