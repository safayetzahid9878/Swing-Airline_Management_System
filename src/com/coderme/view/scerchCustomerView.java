    private void txtAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAddActionPerformed
        
        String gender = null;
        if (rMale.isSelected()) {
            gender = rMale.getText().trim();
        } else {
            gender = rFemale.getText().trim();
        }
        AddCustomer customers = new AddCustomer(Integer.parseInt(txtCustId.getText().trim()),txtFirstname.getText().trim(), txtLastname.getText().trim(),txtNid.getText().trim(), txtPassport.getText().trim(),txtAddress.getText().trim(), gender, txtContact.getText().trim());
        commonService.update(customers);
        //displayDataIntoTable();
        JOptionPane.showMessageDialog(null, "Succeessfully Updated");
              
    }//GEN-LAST:event_txtAddActionPerformed

